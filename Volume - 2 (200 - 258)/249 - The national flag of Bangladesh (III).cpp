#include<bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    float l,w,r,x,y;
    cin>>l;
    w=l*0.6;
    r=l*0.2;
    x=l*0.45;
    y=l*0.3;
    cout<<"L = "<<l;
    cout<<"\nW = "<<w;
    cout<<"\nR = "<<r;
    cout<<fixed<<setprecision(1);
    cout<<"\n(X, Y) = ("<<x<<", "<<y<<")";

    return 0;
}


